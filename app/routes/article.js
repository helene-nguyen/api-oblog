//~ IMPORTATIONS
import { Router } from 'express';
const router = Router();

//~ ROUTE ARTICLE
// GET /posts
router.get('/posts');
// POST /posts
router.post('/posts');

// GET /posts/:id
router.get('/posts/:id');
// PATCH /posts/:id
router.patch('/posts/:id');
// DELETE /posts/:id
router.delete('/posts/:id');

export { router };
